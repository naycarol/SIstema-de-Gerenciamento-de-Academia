    private void initUsuarios() {
        mapaUsuarios = new HashMap<String, Usuario>();
        
        Usuario usuarioAdmin = new Usuario();
        usuarioAdmin.setNome("Admin");
        usuarioAdmin.setLogin("admin");
        usuarioAdmin.setSenha("1234");
        
        mapaUsuarios.put(usuarioAdmin.getLogin() + "#" + usuarioAdmin.getSenha(), usuarioAdmin);
    }
    
    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        String valueLogin = login.getText();
        char[] valueSenha = senha.getPassword();
        
        StringBuilder chave = new StringBuilder(valueLogin);
        chave.append("#");
        chave.append(valueSenha);
        
        if (mapaUsuarios.containsKey(chave.toString())) {
            TelaPrincipal telaPrincipal = new TelaPrincipal();
            telaPrincipal.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Login inv√°lido", "Erro", JOptionPane.ERROR_MESSAGE); 
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

